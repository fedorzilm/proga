/*!
 * \file test_main.cpp
 * \brief Главный файл для запуска всех модульных тестов с использованием Google Test.
 * Этот файл содержит функцию `main`, которая инициализирует Google Test и запускает
 * все тесты, определенные в других файлах `test_*.cpp` проекта.
 * Также может включать глобальные настройки для тестового окружения, если это необходимо,
 * например, инициализацию логгера для всех тестов с определенным уровнем.
 */
#include "gtest/gtest.h"
#include "logger.h" // Для глобальной инициализации логгера, если нужно для всех тестов

int main(int argc, char **argv) {
    // Глобальная инициализация логгера для всех тестов.
    // Уровень можно настроить на DEBUG для получения максимальной информации во время тестов,
    // или на NONE/ERROR, чтобы не загромождать вывод, если тесты проходят успешно.
    // Вывод в файл может быть полезен для анализа результатов после прогона тестов.
    // Logger::init(LogLevel::DEBUG, "unit_tests_run.log");
    // Или, если логгер не нужен для большинства тестов или каждый тест настраивает его сам:
    Logger::init(LogLevel::NONE); // По умолчанию отключаем логирование из библиотеки во время тестов,
                                 // чтобы не мешать выводу gtest, если только тест не требует иное.

    // Инициализация Google Test
    ::testing::InitGoogleTest(&argc, argv);

    // Запуск всех тестов
    return RUN_ALL_TESTS();
}
