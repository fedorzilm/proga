// Предполагаемый путь: src/core/tariff_plan.h
#ifndef TARIFF_PLAN_H
#define TARIFF_PLAN_H

/*!
 * \file tariff_plan.h
 * \brief Определяет класс TariffPlan для управления почасовыми интернет-тарифами
 * с разделением на входящий и исходящий трафик.
 */

#include "common_defs.h" // Для HOURS_IN_DAY и стандартных заголовков
#include <vector>
#include <string>
#include <stdexcept> // Для std::runtime_error, std::invalid_argument, std::out_of_range

/*!
 * \class TariffPlan
 * \brief Управляет почасовыми тарифными ставками для входящего и исходящего интернет-трафика.
 *
 * Этот класс загружает тарифные ставки из конфигурационного файла и предоставляет
 * доступ к этим ставкам. Каждая ставка соответствует стоимости за ГБ для определенного часа суток
 * и определенного направления трафика.
 * Класс помечен как final, так как не предназначен для наследования.
 */
class TariffPlan final {
private:
    std::vector<double> costInPerGBPerHour_{};  /*!< Вектор, хранящий стоимость за ГБ для входящего трафика для каждого из 24 часов. */
    std::vector<double> costOutPerGBPerHour_{}; /*!< Вектор, хранящий стоимость за ГБ для исходящего трафика для каждого из 24 часов. */

public:
    /*!
     * \brief Конструктор по умолчанию.
     * Инициализирует тарифный план, в котором все почасовые ставки (входящие и исходящие) равны 0.0.
     */
    TariffPlan() noexcept;

    /*!
     * \brief Загружает тарифный план из указанного конфигурационного файла.
     * Ожидается, что файл содержит 48 значений типа double:
     * Сначала 24 ставки для входящего трафика (по одному на строку или разделенные пробелами/табуляцией).
     * Затем 24 ставки для исходящего трафика (аналогично).
     * Комментарии, начинающиеся с '#', игнорируются. Пустые строки также игнорируются.
     * \param filename Путь к конфигурационному файлу тарифов.
     * \return true, если загрузка прошла успешно. При ошибках выбрасывает исключения.
     * \throw std::runtime_error если файл не может быть открыт или ошибка чтения.
     * \throw std::invalid_argument если файл не содержит ровно (HOURS_IN_DAY * 2) корректных
     * (неотрицательных, числовых) тарифных ставок или содержит лишние данные после валидных чисел на строке.
     */
    bool loadFromFile(const std::string& filename); // Оставим bool для совместимости с тестами, но ошибки через исключения

    /*!
     * \brief Получает стоимость за ГБ для входящего трафика для указанного часа.
     * \param hour Час суток (0-23).
     * \return Стоимость за ГБ для входящего трафика для указанного часа.
     * \throw std::out_of_range если указанный час некорректен или тарифы не загружены.
     */
    double getCostInForHour(int hour) const;

    /*!
     * \brief Получает стоимость за ГБ для исходящего трафика для указанного часа.
     * \param hour Час суток (0-23).
     * \return Стоимость за ГБ для исходящего трафика для указанного часа.
     * \throw std::out_of_range если указанный час некорректен или тарифы не загружены.
     */
    double getCostOutForHour(int hour) const;

    /*!
     * \brief Получает все почасовые стоимости для входящего трафика.
     * \return Константная ссылка на вектор почасовых стоимостей для входящего трафика.
     */
    const std::vector<double>& getAllInCosts() const noexcept { return costInPerGBPerHour_; }

    /*!
     * \brief Получает все почасовые стоимости для исходящего трафика.
     * \return Константная ссылка на вектор почасовых стоимостей для исходящего трафика.
     */
    const std::vector<double>& getAllOutCosts() const noexcept { return costOutPerGBPerHour_; }
};

#endif // TARIFF_PLAN_H
